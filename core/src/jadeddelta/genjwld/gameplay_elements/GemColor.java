package jadeddelta.genjwld.gameplay_elements;

/**
 * The color of each gem which can be used to distinguished
 * rendering. <code>SPECIAL</code> refers to gems with enhancements
 * that don't have a distinguishing color.
 */
public enum GemColor {
    RED,
    ORANGE,
    YELLOW,
    GREEN,
    BLUE,
    PURPLE,
    WHITE,
    SPECIAL,
    NONE
}
